package go_property_mapper

func GetStrPtr(s string) *string {
	return &s
}

func GetIntPtr(i int) *int {
	return &i
}

func GetInt64Ptr(i int64) *int64 {
	return &i
}

func GetFloatPtr(i float32) *float32 {
	return &i
}

func GetFloat64Ptr(i float64) *float64 {
	return &i
}

func GetBoolPtr(i bool) *bool {
	return &i
}
